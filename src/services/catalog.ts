/**
 * Search catalog by name and category
 */
export function searchCatalog(
  query: string,
  category?: string,
  position?: Position
): Exercise[] {
  const lowerQuery = query.toLowerCase().trim();

  return globalCatalog.filter((exercise) => {
    const matchesQuery = !lowerQuery || exercise.name.toLowerCase().includes(lowerQuery);
    const matchesCategory = !category || exercise.category === category;
    const matchesPosition = !position || !exercise.positionTags || exercise.positionTags.includes(position);

    return matchesQuery && matchesCategory && matchesPosition;
  });
}
